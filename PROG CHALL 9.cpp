#include <iostream>
using namespace std;

int main (){
	
	cout<<"Given these following data types:\n";
	cout<<"char\n";
	cout<<"int\n";
	cout<<"float\n";
	cout<<"double\n";
	cout<<"How much the memory used each?\n";
	cout<<"Size of char is   :"<<sizeof(char)<<"bytes\n";
	cout<<"Size of int is    :"<<sizeof(int)<<"bytes\n";
	cout<<"Size of float is  :"<<sizeof(float)<<"bytes\n";
	cout<<"Size of double is :"<<sizeof(double)<<"bytes\n";
	return 0;
}
